/**
 * Detail about a timezone.
 * Subject to updates of the IANA Timezone database and a rerunning of the 'tztable' utility and integration by TBD.
 * - There are two TimezoneDetail objects per TimezoneEntry.
 * - One is the 'standard' (january) time
 * - the other is the 'daylight' (july) time
 *
 * Not all timezones have a different designation for daylight savings.
 *
 * Jurisdictions that are not in daylight/standard in the july/january time frames are not supported. (are there any of these?)
 * _Note that in the southern hemisphere 'standard / daylight' take opposite meanings, since summer is in January there_
 *
 * @private
 */
export class TimezoneDetail {
    public name:string = ''
    public abbr:string = ''
    public offset:number = 0
}

/**
 * Timezone info per anchor city
 * contains name of anchor and standard/daylight TimezoneDetail data
 *
 * @private
 */
export class TimezoneEntry {
    public anchor:string = ''
    public shortHand:string = ''
    public standard: TimezoneDetail = new TimezoneDetail()
    public daylight: TimezoneDetail = new TimezoneDetail()
}

/**
 * Find the Timezone entry that matches the string given
 * which detail is in effect is not provided by this API
 * To do that requires recording and checking the rules for switch-over date/times
 * and would require us to pass in the date as well, so it could be tested against the rule.
 *
 * @param {string} tzString
 * @return {TimezoneEntry[]} array of entries that match this string
 *
 * @private
 */
export function findTimezone(tzString:string) {
    let found:any = []
    if(tzString === 'UTC') {
        tzString = 'GMT' // use this eqivalent instead, UTC doesn't occur in table
    }
    const tzAbbr = tzString.toUpperCase()
    if(tzAbbr === 'LOCAL') { // use the current local timezone
        let off = new Date().getTimezoneOffset()
        found.push(
            {
                standard: {
                    name: "Current system setting",
                    abbr: "Local",
                    offset: off
                }
            })
        return found
    }


    tztable.forEach(entry => {
        let anchor = entry.anchor
        let short = entry.shortHand && entry.shortHand.toUpperCase()
        let sabbr = entry.standard.abbr.toUpperCase()
        let dabbr = entry.daylight.abbr.toUpperCase()
        let sname = entry.standard.name
        let we = sname.indexOf(' ')
        if(we === -1) we = sname.length
        let ssname = sname.substring(0,we)
        let dname = entry.daylight.name
        we = dname.indexOf(' ')
        if(we === -1) we = dname.length
        let sdname = dname.substring(0,we)
        let city = anchor.substring(anchor.indexOf('/') + 1)
        if (tzString === anchor || tzString === city) {
            found.push(entry)
        }
        if(tzString === sname || tzString === dname) {
            found.push(entry)
        }
        if(tzString === ssname || tzString === sdname) {
            found.push(entry)
        }
        if(tzAbbr == sabbr || tzAbbr == dabbr) {
            found.push(entry)
        }
        if(tzAbbr == short) {
            found.push(entry)
        }
    })
    return found
}

export function timezoneByOffset(tzo:number):string {
    let bestDiff = 999999
    let bestAt = -1
    let tzom = -tzo * 60
    for(let i=0; i<tztable.length; i++) {
        let tzi = tztable[i]
        let diff = Math.abs(tzom - tzi.standard.offset)
        if(diff < bestDiff) {
            bestDiff = diff
            bestAt = i
        }
    }
    let pick = tztable[bestAt]
    if(pick) {
        return pick.anchor
    }
    return ''
}
